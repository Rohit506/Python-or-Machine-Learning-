# lambda function:
# Find square of number using lambda function
square = lambda x: x*x
print(square(5))
print("*"*20)

# Compare two number
compare = lambda x,y: x>y
print(compare(6,7))
print(compare(8,5))
print("*"*20)

# Check the number is odd or even
check = lambda a: a%2==0
print(check(10))
print(check(9))
print("*"*20)